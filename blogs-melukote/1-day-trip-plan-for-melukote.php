        <section class="intro">
            <p>Nestled in the Pandavapura Taluk of Karnataka’s Mandya district, Melukote is a charming heritage town known for its ancient temples, sacred ponds, scenic walks, and traditional food. Rich in history and culture, this serene destination makes for an ideal day trip from Bangalore or Mysore. If you’re planning a short getaway steeped in spirituality and tradition, Melukote deserves a spot on your list.</p>
        </section>

        <section class="history">
            <h2>A Glimpse into History</h2>
            <p>Melukote rose to prominence in the 12th century when Saint Ramanujacharya made it his home for nearly 14 years. During this time, the town became a significant centre for Srivaishnavism, attracting large numbers of Iyengar Brahmins who settled here.</p>
            <p>Legend speaks of a Jain king who once ruled the region and had two daughters, one of whom was gravely ill. Ramanujacharya is believed to have taken her to Thonnur Lake, where she was miraculously cured. As a token of gratitude, the king offered riches, but Ramanujacharya instead requested that he adopt Vaishnavism. The king accepted, took the name Vishnuvardhana, and became a devoted follower. Many of Melukote’s temples were constructed during this era, marking a turning point in the town’s religious history.</p>
        </section>

        <section class="how-to-reach">
            <h2>How to Reach Melukote</h2>
            <p>Melukote lies about 51 km from Mysore and roughly 133 km from Bangalore.</p>
            <ul>
                <li><strong>By Public Transport:</strong> Take a KSRTC bus from Bangalore to Mysore, get down at Mandya, and then board a local bus to Melukote.</li>
                <li><strong>By Car:</strong> If you’re driving, take a right turn shortly after Mandya town. Navigation apps can easily guide you the rest of the way.</li>
            </ul>
        </section>

        <section class="places-to-explore">
            <h2>Places to Explore in Melukote</h2>

            <div class="place">
                <h3>Yoganarasimha Temple</h3>
                <img src="img/yoganarasimha-temple-melukote.png" alt="Yoganarasimha Temple Melukote">
                <p>At the entrance of the town, a left turn leads to the hilltop Yoganarasimha Temple. Visitors can climb nearly 1,000 steps or drive partway and walk the remaining distance recommended due to the steep ascent. The temple enshrines Lord Narasimha in a meditative pose, and the hilltop offers stunning views of the countryside.</p>
                <p><strong>Timings:</strong> 7:00 am – 1:00 pm, 5:00 pm – 8:30 pm</p>
            </div>

            <div class="place">
                <h3>Kalyani (Pushkarani)</h3>
                <img src="img/kalyani-melukote.png" alt="Kalyani (Pushkarani) Melukote">
                <p>Situated at the base of the hill is a beautifully maintained temple pond. Many devotees choose to relax here or take a ritual dip in its sacred waters.</p>
            </div>

            <div class="place">
                <h3>Cheluvanarayana Swamy Temple</h3>
                <img src="img/cheluvanarayana-swamy-temple-melukote.png" alt="Cheluvanarayana Swamy Temple Melukote">
                <p>This is the principal temple of Melukote. Admire the exquisitely carved idol of Cheluvanarayana Swamy and the temple’s expansive corridors supported by ornate pillars.</p>
                <p><strong>Timings:</strong> 12:00 pm – 1:00 pm, 4:00 pm – 8:00 pm</p>
            </div>

            <div class="place">
                <h3>Akka Thangi Kola</h3>
                <img src="img/akka-tangi-kola-melukote.png" alt="Akka Thangi Kola Melukote">
                <p>Behind the Cheluvanarayana Swamy Temple lie two adjacent ponds known as Akka Thangi Kola. According to belief, one contains salty water while the other is fresh a curious and unique sight.</p>
            </div>

            <div class="place">
                <h3>Raya Dwara</h3>
                <img src="img/raya-dwara-melukote.png" alt="Raya Dwara Melukote">
                <p>Perched on a small hill above the twin ponds stands Raya Dwara, an unfinished royal gateway. Folklore says a king ordered it to be completed in a single night. When sunrise arrived before construction was finished, work stopped forever, leaving the structure incomplete.</p>
            </div>

            <div class="place">
                <h3>Sanskrit Academy</h3>
                <img src="img/sanskrit-academy-melukote.png" alt="Sanskrit Academy Melukote">
                <p>Near Raya Dwara is the Sanskrit Academy, home to a library containing a valuable collection of ancient manuscripts and scriptures.</p>
            </div>

            <div class="place">
                <h3>Danush Koti</h3>
                <img src="img/danush-koti-melukote.png" alt="Danush Koti Melukote">
                <p>About 1.5 km ahead on the same road lies Danush Koti, accessible after a short walk of nearly one kilometre. Tradition holds that a crack in the rock here was formed by Lord Rama’s arrow. A natural spring flows through it, keeping the spot lush and peaceful. The viewpoint also offers sweeping vistas of the surrounding farmlands.</p>
            </div>
        </section>

        <section class="nearby-attractions">
            <h2>Nearby Attractions to Add to Your Trip</h2>
            <ul>
                <li><strong>Thonnur Lake (13 km):</strong> A perfect place to enjoy a peaceful sunset</li>
                <li><strong>Thondanoor:</strong> Home to the ancient Sri Nambinarayana Swamy Temple and Sri Gopalakrishna Swamy Temple</li>
                <li><strong>Nagamangala (27 km):</strong> Visit the Hoysala-era Saumyakeshava Temple</li>
                <li><strong>Aghalya (25 km):</strong> Stop at the Malleshwara Temple en route to Shravanabelagola</li>
                <li><strong>Shravanabelagola (34 km):</strong> Trek the hill to see the world’s tallest monolithic statue</li>
            </ul>
            <p>Mandya district is famously known as Karnataka’s “Sugar Bowl.” Along the way, you’ll pass vast sugarcane fields and local jaggery-making units. Take time to interact with villagers, sample fresh sugarcane juice, and buy locally produced jaggery.</p>
