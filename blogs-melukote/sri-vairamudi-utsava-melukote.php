    <h2>History of Sri Vairamudi Utsava</h2>
    <p>
      The festival has roots tracing back several centuries and is closely associated with <a href="https://en.wikipedia.org/wiki/Ramanujacharya" target="_blank" rel="noopener">Sri Ramanujacharya</a>, who promoted the temple as a center of Vaishnavism. 
      The <strong>Vairamudi</strong> crown, literally meaning "diamond crown," symbolizes divine grace and protection. Each year, it is ceremoniously placed on the deity during a grand procession.
    </p>

    <h2>Festival Highlights</h2>
    <ul>
      <li><strong>Grand Processions:</strong> The deity is taken through temple streets amidst devotional music, chants, and floral decorations.</li>
      <li><strong>Rituals and Poojas:</strong> Elaborate poojas and offerings are performed by temple priests according to tradition.</li>
      <li><strong>Cultural Programs:</strong> Classical music, dance performances, and spiritual discourses enrich the festival experience.</li>
      <li><strong>Devotee Participation:</strong> Thousands of pilgrims attend to seek blessings, perform rituals, and immerse in devotion.</li>
    </ul>

