<section class="history">
    <h2>Ancient Origins of Melukote</h2>

    <p>
        Melukote is an ancient temple-town perched on the rocky Yadugiri hill overlooking the
        Kaveri valley in Mandya district, Karnataka. The name <strong>Melukote</strong> literally
        means <em>upper fort</em>, deriving from “Melu” (top) and “Kote” (fort), as the settlement
        grew around a fortified hill site.
    </p>

    <p>
        Archaeological evidence such as megalithic burials and prehistoric line drawings on
        boulders suggests that the Yadugiri range was inhabited long before the historic temple
        town emerged. Local folklore refers to Melukote by ancient names like
        <strong>Narayanadri</strong>, <strong>Vedadri</strong>, and <strong>Yatishaila</strong>
        across the yugas of Hindu cosmology, reflecting its deep mythological significance.
    </p>
</section>

<section class="history">
    <h2>Ramanujacharya and the Rise of Srivaishnavism</h2>

    <p>
        Melukote’s recorded religious prominence began in the 11th–12th century CE with the
        arrival of <strong>Sri Ramanujacharya</strong>, the seminal Vaishnavite philosopher and
        founder of the Vishishtadvaita school.
    </p>

    <p>
        After facing persecution in the Tamil region, Ramanuja travelled along the Kaveri and
        settled in Melukote. Tradition holds that Lord Vishnu appeared to him in a dream and
        revealed the location of sacred clay used for the <em>urdhva pundra</em> (Vaishnava tilak).
        Guided by this vision, Ramanuja discovered a lost <em>vigraha</em> of Vishnu and revitalised
        Melukote as a major centre of Srivaishnava worship, instituting ritual practices that
        continue even today.
    </p>
</section>

<section class="history">
    <h2>Cheluvanarayana Swamy Temple</h2>

    <p>
        At the heart of Melukote stands the <strong>Cheluvanarayana Swamy Temple</strong>, also known
        as <em>Thirunarayanapuram</em>, dedicated to Lord Vishnu in his form as Tirunarayana or
        Cheluvanarayana Swamy.
    </p>

    <p>
        Temple legends describe the metallic <em>utsavamurti</em> as having been worshipped by
        Rama, Krishna, and other divine figures across ages. However, epigraphic evidence recorded
        by the Mysore Archaeological Department indicates that the deity was revered even before
        Ramanuja’s arrival in 1098 CE, suggesting that he restored and revitalised an already
        existing shrine.
    </p>
</section>

<section class="history">
    <h2>Royal Patronage and Temple Expansion</h2>

    <p>
        The present temple complex evolved over centuries through royal patronage. During the
        Vijayanagara Empire, the gopura and temple halls were expanded, while the Wodeyar dynasty
        of Mysore became ardent supporters of the shrine.
    </p>

    <p>
        King <strong>Raja Wodeyar I</strong> granted extensive lands to the temple and gifted the
        first <strong>Rajamudi</strong>, a jewelled crown that became a symbol of ritual splendour.
        Later rulers, including <strong>Krishnaraja Wodeyar III</strong>, added more crowns and
        offerings still used in festivals today.
    </p>

    <p>
        Some historical accounts also suggest that <strong>Tipu Sultan</strong> made symbolic
        offerings, such as ceremonial instruments, highlighting the complex relationship between
        rulers and temple institutions.
    </p>
</section>

<section class="history">
    <h2>Yoga Narasimha Temple</h2>

    <p>
        Above the town, on the hilltop, stands the <strong>Yoga Narasimha Temple</strong>. Though
        much of the present structure dates to the Hoysala period, the site is steeped in legend.
    </p>

    <p>
        Some traditions associate it with Prahlada’s penance and devotion to Narasimha, marking
        it as one of the seven sacred Narasimha kshetras of South India. From the hilltop, visitors
        are rewarded with sweeping views of the Mandya plains and the Kaveri valley.
    </p>
</section>

<section class="history">
    <h2>Centre of Learning and Living Tradition</h2>

    <p>
        Melukote also flourished as a centre of Sanskrit scholarship. The
        <strong>Academy of Sanskrit Research</strong>, established in the 19th century, preserves
        invaluable manuscripts and continues the town’s intellectual legacy.
    </p>

    <p>
        Over centuries, Melukote developed as an <em>agrahara</em> settlement, sustaining temple
        culture through ritual, music, poetry, and festivals. The annual
        <strong>Vairamudi Brahmotsava</strong>, during which the deity is adorned with the jewelled
        crown and taken in procession, draws hundreds of thousands of devotees.
    </p>
</section>

<section class="history">
    <h2>Melukote Today</h2>

    <p>
        Today, Melukote stands as a living chronicle of India’s layered past, from prehistoric
        habitation and medieval devotional reform to royal patronage and enduring Vaishnava
        traditions preserved for more than a millennium.
    </p>
</section>

