Melukote is an ancient temple-town perched on the rocky Yadugiri hill overlooking the Kaveri valley in Mandya district, Karnataka. The name Melukote literally means upper fort — deriving from “Melu” (top) and “Kote” (fort) — because the settlement grew around a fortified hill site. Evidence such as megalithic burials and line drawings on boulders suggests that the Yadugiri range was inhabited by prehistoric communities long before the historic temple town emerged. Local folklore also refers to Melukote by ancient names like Narayanadri, Vedadri and Yatishaila across the yugas of Hindu cosmology, indicating its deep mythological associations. 
