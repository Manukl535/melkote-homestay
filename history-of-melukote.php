The town’s recorded religious prominence truly began in the 11th–12th century CE with the arrival of Sri Ramanujacharya, the seminal Vaishnavite philosopher and founder of the Vishishtadvaita school. After facing persecution in the Tamil region, Ramanuja traveled along the Kaveri and settled in Melukote, where, according to tradition, Lord Vishnu appeared to him in a dream and revealed the location of sacred clay used for the urdhva pundra (Vaishnava tilak). Guided by this vision, Ramanuja uncovered a lost vigraha (idol) of Vishnu and established the town as a centre of Srivaishnava worship, instituting ritual practices that continue to this day. 
