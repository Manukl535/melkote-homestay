At the heart of Melukote’s spiritual landscape is the Cheluvanarayana Swamy Temple (also called Thirunarayanapuram), dedicated to Lord Vishnu in his form of Tirunarayana or Cheluvanarayana Swamy. While local legend and temple tradition describe the metallic utsavamurti (festival idol) as having been worshipped by Rama, Krishna, and other divine figures across ages, epigraphic evidence recorded in the Mysore Archaeological Department indicates that the deity was already revered long before Ramanuja’s arrival in 1098 CE — suggesting that Ramanuja restored or revitalized an existing shrine rather than creating it from scratch. 
