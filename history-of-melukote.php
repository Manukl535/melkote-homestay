The temple complex that stands today was built and expanded over centuries through royal patronage. During the Vijayanagara Empire, the gopura and temple halls were enhanced, while the Wodeyar dynasty of Mysore became ardent supporters of the shrine. King Raja Wodeyar I (r. late 16th–early 17th century) granted the temple large estates and gifted the first Rajamudi — a jewelled crown that became a symbol of ritual splendor. Later, his successors, including Krishnaraja Wodeyar III, contributed additional crowns and offerings that are still used in festivals today. Some accounts also suggest that Tipu Sultan made symbolic contributions such as a drum or other offerings, illustrating the complex historical interactions between rulers and temple institutions. 
