A Perfect Day Trip to Melukote

Nestled in the Pandavapura Taluk of Karnataka’s Mandya district, Melukote is a charming heritage town known for its ancient temples, sacred ponds, scenic walks, and traditional food. Rich in history and culture, this serene destination makes for an ideal day trip from Bangalore or Mysore. If you’re planning a short getaway steeped in spirituality and tradition, Melukote deserves a spot on your list.

A Glimpse into History

Melukote rose to prominence in the 12th century when Saint Ramanujacharya made it his home for nearly 14 years. During this time, the town became a significant centre for Srivaishnavism, attracting large numbers of Iyengar Brahmins who settled here.

Legend speaks of a Jain king who once ruled the region and had two daughters, one of whom was gravely ill. Ramanujacharya is believed to have taken her to Thonnur Lake, where she was miraculously cured. As a token of gratitude, the king offered riches, but Ramanujacharya instead requested that he adopt Vaishnavism. The king accepted, took the name Vishnuvardhana, and became a devoted follower. Many of Melukote’s temples were constructed during this era, marking a turning point in the town’s religious history.

How to Reach Melukote

Melukote lies about 51 km from Mysore and roughly 133 km from Bangalore.

By Public Transport: Take a KSRTC bus from Bangalore to Mysore, get down at Mandya, and then board a local bus to Melukote.

By Car: If you’re driving, take a right turn shortly after Mandya town. Navigation apps can easily guide you the rest of the way.

Places to Explore in Melukote
Yoganarasimha Temple

At the entrance of the town, a left turn leads to the hilltop Yoganarasimha Temple. Visitors can climb nearly 1,000 steps or drive partway and walk the remaining distance—recommended due to the steep ascent. The temple enshrines Lord Narasimha in a meditative pose, and the hilltop offers stunning views of the countryside.
Timings: 7:00 am – 1:00 pm, 5:00 pm – 8:30 pm

Kalyani (Pushkarani)

Situated at the base of the hill is a beautifully maintained temple pond. Many devotees choose to relax here or take a ritual dip in its sacred waters.

Cheluvanarayana Swamy Temple

This is the principal temple of Melukote. Admire the exquisitely carved idol of Cheluvanarayana Swamy and the temple’s expansive corridors supported by ornate pillars.
Timings: 12:00 pm – 1:00 pm, 4:00 pm – 8:00 pm
If you arrive in the afternoon, explore nearby attractions that remain open during the day and return here later.

Taste of Local Cuisine

Melukote is well known for its Iyengar Brahmin food. Several small eateries serve authentic local dishes throughout the day. Don’t miss out on Puliogare, Sweet Pongal, and Sheera Anna. Subbana Mess is a popular spot to sample these traditional flavors.

Akka Thangi Kola

Behind the Cheluvanarayana Swamy Temple lie two adjacent ponds known as Akka Thangi Kola. According to belief, one contains salty water while the other is fresh—a curious and unique sight.

Raya Dwara

Perched on a small hill above the twin ponds stands Raya Dwara, an unfinished royal gateway. Folklore says a king ordered it to be completed in a single night. When sunrise arrived before construction was finished, work stopped forever, leaving the structure incomplete.

Sanskrit Academy

Near Raya Dwara is the Sanskrit Academy, home to a library containing a valuable collection of ancient manuscripts and scriptures.

Danush Koti

About 1.5 km ahead on the same road lies Danush Koti, accessible after a short walk of nearly one kilometre. Tradition holds that a crack in the rock here was formed by Lord Rama’s arrow. A natural spring flows through it, keeping the spot lush and peaceful. The viewpoint also offers sweeping vistas of the surrounding farmlands.

Nearby Attractions to Add to Your Trip

While Melukote itself can be explored in half a day, you can extend your journey with nearby destinations:

Thonnur Lake (13 km): A perfect place to enjoy a peaceful sunset

Thondanoor: Home to the ancient Sri Nambinarayana Swamy Temple and Sri Gopalakrishna Swamy Temple

Nagamangala (27 km): Visit the Hoysala-era Saumyakeshava Temple

Aghalya (25 km): Stop at the Malleshwara Temple en route to Shravanabelagola

Shravanabelagola (34 km): Trek the hill to see the world’s tallest monolithic statue

Mandya district is famously known as Karnataka’s “Sugar Bowl.” Along the way, you’ll pass vast sugarcane fields and local jaggery-making units. Take time to interact with villagers, sample fresh sugarcane juice, and buy locally produced jaggery.

